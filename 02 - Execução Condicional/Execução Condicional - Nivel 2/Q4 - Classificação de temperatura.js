// Dada a temperatura, classificá-la como fria (< 15), quente (> 30) e normal (caso contrário);

let temperatura = 16

if(temperatura <= 15){
	console.log(temperatura, 'temperatura fria')
}else if(temperatura >= 30){
	console.log(temperatura, 'temperatura quente')
}else {
	console.log(temperatura, 'temperatura normal')
}

