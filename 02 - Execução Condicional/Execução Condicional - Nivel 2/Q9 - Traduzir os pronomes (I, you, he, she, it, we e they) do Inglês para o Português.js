// Traduzir os pronomes (I, you, he, she, it, we e they) do Inglês para o Português;

let pronome = 'it'

if (pronome == 'i'){
	console.log('eu')
} else if (pronome == 'you'){
	console.log('voce')
} else if (pronome == 'he'){
	console.log('ele')
} else if (pronome == 'she'){
	console.log('ela')
} else if (pronome == 'it'){
	console.log('ele')
} else if (pronome == 'we'){
	console.log('nos')
} else {
	console.log('eles ou elas')
}


