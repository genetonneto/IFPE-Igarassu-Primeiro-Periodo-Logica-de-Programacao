// Testar se o quadrado de um número é maior do que ele multiplicado por 10;

let numero = 7;
let quadrado = numero * numero
let multiplica = numero * 10

if (quadrado > multiplica){
	console.log('Quadrado eh maior')
} else if (quadrado == multiplica){
	console.log('Os valores sao iguais')
} else {
	console.log('Multiplicado por 10 e maior')
}



